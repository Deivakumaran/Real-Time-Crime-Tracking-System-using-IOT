    private void requestAttorneyButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_requestAttorneyButtonActionPerformed
        // TODO add your handling code here:
        int row = firTable.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(null, "Pls select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Organization org = null;
        EnquiryReportRequest enquiryReportRequest = (EnquiryReportRequest) firTable.getValueAt(row, 0);
        if (enquiryReportRequest.getStatus().equalsIgnoreCase("Warrant Pending")) {

            for (Enterprise attorney : system.getNetworkList().get(0).getEnterpriseDirectory().getEnterpriseList()) {
                for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
                    if (organization instanceof AttorneyOrganization) {
                        org = organization;
                        break;
                    }
                }
            }
            if (org != null) {
                enquiryReportRequest.setStatus("Attorney Review Pending");
                org.getWorkQueue().getWorkRequestList().add(enquiryReportRequest);
                JOptionPane.showMessageDialog(null, "Request sent");

            }
            populateTable();
        } else {
            JOptionPane.showMessageDialog(null, "Cannot send the same request", "WARNING", JOptionPane.WARNING_MESSAGE);

        }
    }//GEN-LAST:event_requestAttorneyButtonActionPerformed

